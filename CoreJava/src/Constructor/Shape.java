package Constructor;

public class Shape {
	public Shape() {
		System.out.println("WASSUPP!");
	}

	public Shape(int i) {
		System.out.println("HEY THEREEE! " + i);
	}

	public Shape(int i, int j) {
		System.out.println("HEY THEREEE! " + i + " " + j);
	}

	public Shape(int i, int j, int k) {
		System.out.println("HEY THEREEE! " + i + " " + j + " " + k);
	}
}
